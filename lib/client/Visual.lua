---@class Visual
Visual = {}

--- Triggers a help Box in the top left corner of the screen. 
---@param text string
---@param bleep boolean (sound)
function Visual.ShowHelp(text, bleep)
    BeginTextCommandDisplayHelp("STRING")
    AddTextComponentSubstringPlayerName(text)
    EndTextCommandDisplayHelp(0, false, bleep, -1)
end

--- Triggers a native notification, nothing to fancy.
---@param text string
function Visual.Notify(text)
    SetNotificationTextEntry("STRING")
    AddTextComponentString(text)
    DrawNotification(true, true)
end